$('#btnSlideUp').click(function () {
    let mainDiv = $('#mainDiv');
    mainDiv.slideUp();
});

$('#btnSlideDown').click(function () {
    let mainDiv = $('#mainDiv');
    mainDiv.slideDown();
});

$('#btnSlieToggle').click(function () {
    let mainDiv = $('#mainDiv');
    mainDiv.slideToggle();
});